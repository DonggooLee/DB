-- USER_IND_COLUMNS 데이터 딕셔너리 뷰
SELECT INDEX_NAME, TABLE_NAME, COLUMN_NAME
FROM USER_IND_COLUMNS
WHERE TABLE_NAME IN('EMP', 'DEPT');
DROP TABLE emp01;
CREATE TABLE emp01 AS SELECT * FROM emp;

-- 데이터 자가복제 (연습용)
