SELECT * FROM EMP01;

-- 검색용 데이터 삽입
INSERT INTO EMP01(EMPNO, ENAME) VALUES(1111,'홍길동');
SET TIMING ON
SELECT DISTINCT EMPNO, ENAME FROM EMP01 WHERE ENAME = '홍길동';

-- 인덱스 생성
CREATE INDEX IDX_EMP01_ENAME ON EMP01(ENAME); -- 특정 컬럼에 인덱스 부여 한다
SELECT DISTINCT EMPNO, ENAME FROM EMP01 WHERE ENAME = '홍길동';

